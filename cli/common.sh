# A set of common bash commands
#
# In regards to writing high-quality "usage" messages please refer to:
# http://pubs.opengroup.org/onlinepubs/9699919799/basedefs/V1_chap12.html

readonly T_BOLD=`tput bold`
readonly T_NORMAL=`tput sgr0`
readonly T_ERROR=`tput setab 1`   # red background


# ==============================================================================
# Output all given Messages to STDERR
# ------------------------------------------------------------------------------
printError() {
    echo -e " !     ERROR: ${*}" >&2
}
# ==============================================================================


# ==============================================================================
#
# ------------------------------------------------------------------------------
printHeader() {
    local -r sTitle="$1"
    local -r iWidth=80

    echo ''
    echo '================================================================='
    printf "%*s\n" $(((${#sTitle}+$iWidth)/2)) "${T_BOLD}${sTitle}${T_NORMAL}"
    echo '-----------------------------------------------------------------'
}
# ==============================================================================


# ==============================================================================
#
# ------------------------------------------------------------------------------
printStatus() {
    echo -e "-----> $*"
}
# ==============================================================================


# ==============================================================================
#
# ------------------------------------------------------------------------------
printTopic() {
    echo -e "\n=====> $*"
}
# ==============================================================================


# ==============================================================================
#
# ------------------------------------------------------------------------------
printWarning() {
    echo -e " !     WARNING: $*"
}
# ==============================================================================


# ==============================================================================
# Displays all lines in main script that start with '##'
# ------------------------------------------------------------------------------
usageShort() {
    # shellcheck disable=SC2086
    [ "$*" ] && echo "$(basename $0): $*"
    sed -n '/^##/,/^$/s/^## \{0,1\}//p' "$0"
}
# ==============================================================================


# ==============================================================================
# Displays all lines in main script that start with '#/'
# ------------------------------------------------------------------------------
usageFull() {
    grep '^#/' <"$0" | cut -c4-
}
# ==============================================================================

